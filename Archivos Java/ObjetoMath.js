Math.round (0.49); //acá el programa va a readondear al numero o al decimal mas cercano, en este caso al 0.5
Math.round (0.50); //acá directamente redondeará al numero 1
Math.floor (5.93); //acá siempre se va a readondear al numero entero mas bajo, en este caso al 5
Math.ceil (3.27); //acá se redondeará al numero entero mas alto, en este caso al 4

Math.max(1, 2, 3, 4, 5); //acá lo que va a suceder es que nos va a indicar el numero mas alto
Math.min(1, 2, 3, 4, 5); //acá hará lo contrario a lo anterior, mostrará el nmero mas bajo

